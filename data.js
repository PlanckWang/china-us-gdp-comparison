// 格式化函数，用于显示数值
function formatNumber(num) {
    if (num === null || num === undefined) {
        return '无数据';
    }
    
    // 对于万亿级数据，显示为万亿
    if (Math.abs(num) >= 1000) {
        return (num / 1000).toFixed(2) + '万亿';
    }
    // 对于十亿级数据，显示为十亿
    else {
        return num.toFixed(2) + '十亿';
    }
}

// 格式化百分比
function formatPercent(num) {
    if (num === null || num === undefined) {
        return '无数据';
    }
    return num.toFixed(2) + '%';
}